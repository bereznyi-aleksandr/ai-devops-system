#!/usr/bin/env python3
from __future__ import annotations

import json
import sys
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
RESULTS_DIR = ROOT / 'governance' / 'runtime' / 'results'


class FailureWriterError(RuntimeError):
    pass


def utc_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace('+00:00', 'Z')


def main() -> int:
    try:
        if len(sys.argv) < 4:
            raise FailureWriterError(
                'Usage: system_failure_result_writer_v1.py <ROLE> <TASK_ID> <ERROR_DETAILS> [RESULT]'
            )

        role = str(sys.argv[1]).strip().upper()
        task_id = str(sys.argv[2]).strip()
        error_details = str(sys.argv[3]).strip()
        error_class = str(sys.argv[4]).strip() if len(sys.argv) >= 5 else ''
        result_value = str(sys.argv[5]).strip().upper() if len(sys.argv) >= 6 else (str(sys.argv[4]).strip().upper() if len(sys.argv) == 5 and str(sys.argv[4]).strip().upper() in {'FAILURE', 'BLOCKED'} else 'FAILURE')

        if role not in {'EXECUTOR', 'AUDITOR'}:
            raise FailureWriterError(f'Unsupported role: {role}')
        if not task_id:
            raise FailureWriterError('TASK_ID is empty')
        if result_value not in {'FAILURE', 'BLOCKED'}:
            raise FailureWriterError(f'Unsupported result: {result_value}')

        RESULTS_DIR.mkdir(parents=True, exist_ok=True)

        out_path = RESULTS_DIR / f'{role.lower()}_failure_result.json'
        payload = {
            'result_manifest_version': 'v1',
            'task_id': task_id,
            'role': role,
            'result': result_value,
            'ts_utc': utc_now(),
            'summary': 'automatic failure result generated by runner',
            'error_class': error_class,
            'error_details': error_details,
        }

        out_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')

        print(json.dumps({
            'system_failure_result_writer_version': 'v1',
            'result': 'SUCCESS',
            'role': role,
            'task_id': task_id,
            'written_manifest': str(out_path.relative_to(ROOT)),
            'applied_result_value': result_value,
        }, ensure_ascii=False, indent=2))
        return 0

    except Exception as e:
        print(json.dumps({
            'system_failure_result_writer_version': 'v1',
            'result': 'BLOCKED',
            'error': str(e),
        }, ensure_ascii=False, indent=2), file=sys.stderr)
        return 1


if __name__ == '__main__':
    raise SystemExit(main())
